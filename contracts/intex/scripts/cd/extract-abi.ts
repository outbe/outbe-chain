// Extract ABIs from Hardhat artifacts to dist/abi directory
// Used during package publishing to create clean ABI exports

import * as fs from "fs";
import * as path from "path";

const ARTIFACTS_DIR = "artifacts/contracts";
const DIST_DIR = "dist";
const OUTPUT_DIR = "dist/abi";

// Contracts to extract ABIs for
const CONTRACTS = [
  // BNB-side
  "bnb/IntexAuction",
  "bnb/EscrowAdapter",
  "bnb/TargetMessenger",
  // Outbe-side
  "outbe/IntexSettlement",
  "outbe/Desis",
  "outbe/IntexFactory",
  "outbe/OriginMessenger",
  // Both chains
  "shared/IntexNFT1155",
  "shared/ONFT1155Adapter",
  "shared/ONFT1155AdapterBatch",
];

const INTERFACES = [
  // BNB-side
  "bnb/interfaces/IIntexAuction",
  "bnb/interfaces/IEscrowAdapter",
  "bnb/interfaces/ITargetMessenger",
  // Outbe-side
  "outbe/interfaces/IIntexSettlement",
  "outbe/interfaces/IDesis",
  "outbe/interfaces/IIntexFactory",
  "outbe/interfaces/IOriginMessenger",
  // Both chains
  "shared/interfaces/IIntexNFT1155",
  "shared/interfaces/IONFT1155Adapter",
  "shared/interfaces/IONFT1155AdapterBatch",
];

interface ArtifactJson {
  abi: unknown[];
  contractName: string;
  sourceName: string;
}

function extractAbi(contractPath: string): void {
  const parts = contractPath.split("/");
  const contractName = parts[parts.length - 1];
  const artifactPath = path.join(
    ARTIFACTS_DIR,
    `${contractPath}.sol`,
    `${contractName}.json`
  );

  if (!fs.existsSync(artifactPath)) {
    console.warn(`Warning: Artifact not found for ${contractPath} at ${artifactPath}`);
    return;
  }

  const artifact: ArtifactJson = JSON.parse(fs.readFileSync(artifactPath, "utf-8"));

  if (!artifact.abi || artifact.abi.length === 0) {
    console.warn(`Warning: Empty ABI for ${contractPath}`);
    return;
  }

  const outputPath = path.join(OUTPUT_DIR, `${contractName}.json`);
  
  // Create output with just ABI and contract name for cleaner exports
  const output = {
    contractName: artifact.contractName,
    abi: artifact.abi,
  };

  fs.writeFileSync(outputPath, JSON.stringify(output, null, 2));
  console.log(`✓ Extracted ABI: ${contractName} (${artifact.abi.length} entries)`);
}

function main(): void {
  console.log("Extracting ABIs from artifacts...\n");

  // Ensure output directory exists
  fs.mkdirSync(OUTPUT_DIR, { recursive: true });

  // Extract contract ABIs
  console.log("Contracts:");
  for (const contract of CONTRACTS) {
    extractAbi(contract);
  }

  // Extract interface ABIs
  console.log("\nInterfaces:");
  for (const iface of INTERFACES) {
    extractAbi(iface);
  }

  // Create an index file in dist/abi/ for direct ABI subpath imports
  const abiIndexContent = [
    "// Auto-generated ABI exports",
    "",
    ...CONTRACTS.map((c) => {
      const name = c.split("/").pop();
      return `export { default as ${name}ABI } from './${name}.json';`;
    }),
    "",
    ...INTERFACES.map((i) => {
      const name = i.split("/").pop();
      return `export { default as ${name}ABI } from './${name}.json';`;
    }),
    "",
  ].join("\n");

  fs.writeFileSync(path.join(OUTPUT_DIR, "index.ts"), abiIndexContent);
  console.log("\n✓ Created ABI index file");

  // Create the package entry points (dist/index.js + dist/index.d.ts).
  // Kept here so the export list stays in sync with CONTRACTS + INTERFACES.
  const allNames = [...CONTRACTS, ...INTERFACES].map(
    (c) => c.split("/").pop() as string
  );

  const indexJs = [
    "// Auto-generated by scripts/cd/extract-abi.ts — do not edit by hand.",
    ...allNames.map(
      (n) => `import ${n}ABI from './abi/${n}.json' with { type: 'json' };`
    ),
    "",
    `export { ${allNames.map((n) => `${n}ABI`).join(", ")} };`,
    "",
    "export const loadAddresses = async (network) => {",
    "  const module = await import(`./addresses/${network}.json`, { with: { type: 'json' } });",
    "  return module.default;",
    "};",
    "",
  ].join("\n");

  const indexDts = [
    "// Auto-generated by scripts/cd/extract-abi.ts — do not edit by hand.",
    ...allNames.map(
      (n) =>
        `export declare const ${n}ABI: { contractName: string; abi: unknown[] };`
    ),
    "export declare const loadAddresses: (network: string) => Promise<{",
    "  contracts: Record<string, string>;",
    "  tokens: Record<string, string>;",
    "  external: Record<string, string>;",
    "}>;",
    "",
  ].join("\n");

  fs.mkdirSync(DIST_DIR, { recursive: true });
  fs.writeFileSync(path.join(DIST_DIR, "index.js"), indexJs);
  fs.writeFileSync(path.join(DIST_DIR, "index.d.ts"), indexDts);
  console.log(`✓ Created package index with ${allNames.length} ABI exports`);

  console.log("\nABI extraction complete!");
}

main();
